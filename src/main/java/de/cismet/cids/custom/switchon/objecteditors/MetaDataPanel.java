    /**
     * Print no if the value is null, otherwise yes.
     *
     * @version  $Revision$, $Date$
     */
    private class NullCellRenderer extends DefaultTableCellRenderer {

        //~ Methods ------------------------------------------------------------

        @Override
        protected void setValue(final Object value) {
            if (value == null) {
                this.setText("No");
            } else {
                this.setText("Yes");
            }
        }
    }
