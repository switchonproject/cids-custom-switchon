    /**
     * DOCUMENT ME!
     *
     * @return  DOCUMENT ME!
     */
    public String getConfigurationString() {
        if (rbtnBasic.isSelected()) {
            return "basic";
        } else if (rbtnAdvanced.isSelected()) {
            return "advanced";
        } else if (rbtnExpert.isSelected()) {
            return "expert";
        } else {
            LOG.error("Should never happen. 'basic' is returned.");
            return "basic";
        }
    }

    /**
     * DOCUMENT ME!
     *
     * @param  configuration  DOCUMENT ME!
     */
    public void setConfiguration(final String configuration) {
        if (configuration != null) {
            switch (configuration) {
                case "basic": {
                    rbtnBasic.setSelected(true);
                    break;
                }
                case "advanced": {
                    rbtnAdvanced.setSelected(true);
                    break;
                }
                case "expert": {
                    rbtnExpert.setSelected(true);
                    break;
                }
            }
        }
    }

